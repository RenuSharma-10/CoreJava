
public class ArithmeticOperation {
	public static void main(String[] args) {
		int a = 34;
		int b = 56;
		int sum, sub, mul, div, mod;
		sum = a + b;
		sub = a - b;
		mul = a * b;
		div = a / b;
		mod = a % b;
		System.out.println("Sum=>" + sum + "Sub=>" + sub + "Mul=>" + mul + "Div=>" + div + "Mod=>" + mod);
	}
}
